// variable that holds my age which is constant
const myAge = 21;

// variable that could change
var earlyYears = 2;

earlyYears *= 10.5;

// subtract 2 years from my age
var laterYears = myAge - 2;

// multiply laterYears by 4
laterYears *= 4;

// added earlyYears and laterYears
var myAgeInDogYears = earlyYears + laterYears;

// variable that holds my name in Lowercase
const myName = 'Adham'.toLowerCase();

// print name , age as a human and age as a dog
console.log("My name is " + myName + ". I am " + myAge + " years old in human years which is " + myAgeInDogYears + " years old in dog years.");